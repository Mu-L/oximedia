//! AI-powered metadata enrichment for media assets.
//!
//! Provides rule-based and structural enrichment of asset metadata without
//! requiring a live inference server.  When the full `oximedia-neural` scene
//! classifier is available (feature-gated), results from the neural backend
//! are merged with the rule-based layer.
//!
//! # Architecture
//! ```text
//!  ┌────────────────────────────────┐
//!  │  MetadataEnricher              │
//!  │  ┌─────────────┐              │
//!  │  │ RuleEngine  │  filename /  │
//!  │  │             │  extension   │
//!  │  └─────────────┘  patterns    │
//!  │  ┌─────────────┐              │
//!  │  │ NeuralBackend│  (optional) │
//!  │  └─────────────┘              │
//!  └────────────────────────────────┘
//!         │ EnrichedMetadata
//!         ▼
//! ```
//!
//! # Example
//! ```rust
//! use oximedia_mam::ai_enrichment::{AiEnrichmentConfig, MetadataEnricher};
//!
//! let cfg = AiEnrichmentConfig::default();
//! let enricher = MetadataEnricher::new(cfg);
//! let result = enricher.enrich("holiday_video.mp4", b"");
//! assert!(!result.tags.is_empty());
//! ```

