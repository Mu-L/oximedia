/// Result from the neural scene classifier.
///
/// In a production system this would be populated by calling into
/// `oximedia-neural`'s scene classification pipeline with the decoded
/// frame data.  Here we provide a pure-Rust placeholder that always returns
/// an empty result so the crate compiles without an ML runtime dependency.
#[derive(Debug, Clone, Default)]
pub struct NeuralClassifierResult {
    /// Scene class labels.
    pub labels: Vec<String>,
    /// Confidence scores, aligned with `labels`.
    pub scores: Vec<f32>,
}

/// Thin wrapper around the neural scene classifier.
///
/// Implement `classify_bytes` to connect to `oximedia-neural` when that
/// crate is available as a workspace dependency.
#[derive(Debug, Clone, Default)]
pub struct NeuralBackend;

impl NeuralBackend {
    /// Create a new backend.
    pub fn new() -> Self {
        Self
    }

    /// Classify a media asset from its raw bytes.
    ///
    /// The current implementation is a rule-based stub that inspects the
    /// first few bytes (magic numbers) and returns coarse labels.  When
    /// `oximedia-neural` is wired in, replace this body with an actual
    /// forward pass through the scene-classification model.
    pub fn classify_bytes(&self, filename: &str, data: &[u8]) -> NeuralClassifierResult {
        let mut labels = Vec::new();
        let mut scores = Vec::new();

        // Detect basic media type from magic bytes.
        let media_type = detect_media_type_from_magic(data);
        if let Some(mt) = media_type {
            labels.push(mt.to_string());
            scores.push(0.90);
        }

        // Supplement from filename extension as a fallback.
        let lower = filename.to_lowercase();
        if lower.ends_with(".mp4") || lower.ends_with(".mov") || lower.ends_with(".mkv") {
            if !labels.contains(&"video".to_string()) {
                labels.push("video".to_string());
                scores.push(0.85);
            }
        } else if lower.ends_with(".mp3") || lower.ends_with(".flac") || lower.ends_with(".wav") {
            if !labels.contains(&"audio".to_string()) {
                labels.push("audio".to_string());
                scores.push(0.85);
            }
        } else if lower.ends_with(".jpg") || lower.ends_with(".jpeg") || lower.ends_with(".png") {
            if !labels.contains(&"image".to_string()) {
                labels.push("image".to_string());
                scores.push(0.85);
            }
        }

        NeuralClassifierResult { labels, scores }
    }
}

/// Detect the broad media type from the first few magic bytes.
fn detect_media_type_from_magic(data: &[u8]) -> Option<&'static str> {
    if data.len() < 4 {
        return None;
    }
    match &data[..4] {
        // JPEG: FF D8 FF
        [0xFF, 0xD8, 0xFF, _] => Some("image"),
        // PNG: 89 50 4E 47
        [0x89, 0x50, 0x4E, 0x47] => Some("image"),
        // MP3 ID3
        [0x49, 0x44, 0x33, _] => Some("audio"),
        // RIFF (WAV, AVI)
        [0x52, 0x49, 0x46, 0x46] => {
            if data.len() >= 12 {
                match &data[8..12] {
                    [0x57, 0x41, 0x56, 0x45] => Some("audio"), // WAVE
                    [0x41, 0x56, 0x49, 0x20] => Some("video"), // AVI
                    _ => Some("audio"),
                }
            } else {
                Some("audio")
            }
        }
        // fLaC
        [0x66, 0x4C, 0x61, 0x43] => Some("audio"),
        // PDF
        [0x25, 0x50, 0x44, 0x46] => Some("document"),
        _ => None,
    }
}

