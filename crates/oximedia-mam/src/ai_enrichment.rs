/// A single enrichment rule that matches on filename/extension patterns.
#[derive(Debug, Clone)]
pub struct EnrichmentRule {
    /// A substring that must appear in the lowercased filename (including extension)
    /// for this rule to fire.  Use `""` to always match.
    pub pattern: String,
    /// Tags to inject when the rule matches.
    pub tags: Vec<String>,
    /// Confidence assigned to injected tags.
    pub confidence: f32,
    /// Category to inject (optional).
    pub category: Option<String>,
    /// Extra metadata key/value pairs to inject.
    pub extra: HashMap<String, String>,
}

impl EnrichmentRule {
    /// Create a simple tag-injection rule.
    pub fn new(pattern: impl Into<String>, tags: Vec<String>, confidence: f32) -> Self {
        Self {
            pattern: pattern.into(),
            tags,
            confidence,
            category: None,
            extra: HashMap::new(),
        }
    }

    /// Check whether this rule matches the given lowercased filename.
    pub fn matches(&self, lower_name: &str) -> bool {
        self.pattern.is_empty() || lower_name.contains(self.pattern.as_str())
    }
}

/// Rule-based enrichment engine.
///
/// Applies a list of [`EnrichmentRule`]s in order against the lowercased
/// filename and accumulates tags, categories, and captions.
#[derive(Debug, Clone, Default)]
pub struct RuleEngine {
    rules: Vec<EnrichmentRule>,
}

impl RuleEngine {
    /// Create an engine with an empty rule set.
    pub fn new() -> Self {
        Self::default()
    }

    /// Add a rule to the engine.
    pub fn add_rule(&mut self, rule: EnrichmentRule) {
        self.rules.push(rule);
    }

    /// Apply all matching rules to `filename` and return the accumulated result.
    pub fn apply(&self, filename: &str) -> EnrichedMetadata {
        let lower = filename.to_lowercase();
        let mut result = EnrichedMetadata::default();

        for rule in &self.rules {
            if rule.matches(&lower) {
                for tag in &rule.tags {
                    if !result.tags.contains(tag) {
                        result.tags.push(tag.clone());
                        result.confidence.push(rule.confidence);
                    }
                }
                if let Some(ref cat) = rule.category {
                    if !result.categories.contains(cat) {
                        result.categories.push(cat.clone());
                    }
                }
                for (k, v) in &rule.extra {
                    result.extra.entry(k.clone()).or_insert_with(|| v.clone());
                }
            }
        }
        result
    }
}

/// Build the default rule set that covers common media filename patterns.
fn default_rule_engine() -> RuleEngine {
    let mut engine = RuleEngine::new();

    // ── Extension-based rules ──────────────────────────────────────────────
    let ext_rules: &[(&str, &[&str], f32, &str)] = &[
        (".mp4", &["video", "mp4"], 0.95, "video"),
        (".mov", &["video", "quicktime"], 0.95, "video"),
        (".mkv", &["video", "matroska"], 0.95, "video"),
        (".avi", &["video", "avi"], 0.90, "video"),
        (".webm", &["video", "webm"], 0.90, "video"),
        (".mp3", &["audio", "mp3"], 0.95, "audio"),
        (".flac", &["audio", "flac", "lossless"], 0.95, "audio"),
        (".wav", &["audio", "wav", "pcm"], 0.95, "audio"),
        (".ogg", &["audio", "ogg"], 0.90, "audio"),
        (".jpg", &["image", "jpeg"], 0.95, "image"),
        (".jpeg", &["image", "jpeg"], 0.95, "image"),
        (".png", &["image", "png"], 0.95, "image"),
        (".tiff", &["image", "tiff", "raw"], 0.90, "image"),
        (".dng", &["image", "raw", "dng"], 0.90, "image"),
        (".pdf", &["document", "pdf"], 0.95, "document"),
    ];

    for (ext, tags, conf, cat) in ext_rules {
        let mut rule =
            EnrichmentRule::new(*ext, tags.iter().map(|t| t.to_string()).collect(), *conf);
        rule.category = Some(cat.to_string());
        engine.add_rule(rule);
    }

    // ── Keyword-based subject rules ────────────────────────────────────────
    let kw_rules: &[(&str, &[&str], f32, &str)] = &[
        (
            "interview",
            &["interview", "talking-head"],
            0.80,
            "interview",
        ),
        (
            "tutorial",
            &["tutorial", "educational"],
            0.80,
            "educational",
        ),
        ("trailer", &["trailer", "promo"], 0.85, "promo"),
        ("timelapse", &["timelapse", "time-lapse"], 0.85, "timelapse"),
        ("broll", &["b-roll", "supplemental"], 0.75, "b-roll"),
        ("b_roll", &["b-roll", "supplemental"], 0.75, "b-roll"),
        ("highlight", &["highlight", "reel"], 0.80, "highlight"),
        ("behind", &["bts", "behind-the-scenes"], 0.80, "bts"),
        ("bts", &["bts", "behind-the-scenes"], 0.85, "bts"),
        ("livestream", &["live", "stream"], 0.80, "live"),
        (
            "screengrab",
            &["screen-recording"],
            0.80,
            "screen-recording",
        ),
        ("screencap", &["screen-recording"], 0.80, "screen-recording"),
        ("drone", &["aerial", "drone"], 0.85, "aerial"),
        ("aerial", &["aerial"], 0.85, "aerial"),
        ("macro", &["macro", "close-up"], 0.80, "macro"),
        ("slowmo", &["slow-motion"], 0.85, "slow-motion"),
        ("slo_mo", &["slow-motion"], 0.85, "slow-motion"),
        ("4k", &["4k", "uhd"], 0.80, "uhd"),
        ("uhd", &["uhd", "4k"], 0.80, "uhd"),
        ("hdr", &["hdr"], 0.80, "hdr"),
    ];

    for (kw, tags, conf, cat) in kw_rules {
        let mut rule =
            EnrichmentRule::new(*kw, tags.iter().map(|t| t.to_string()).collect(), *conf);
        rule.category = Some(cat.to_string());
        engine.add_rule(rule);
    }

    engine
}

