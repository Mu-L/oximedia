/// Generate a short, human-readable caption from enriched data.
fn generate_caption(filename: &str, tags: &[String], categories: &[String]) -> String {
    let stem = std::path::Path::new(filename)
        .file_stem()
        .and_then(|s| s.to_str())
        .unwrap_or(filename)
        .replace(['_', '-'], " ");

    if !categories.is_empty() && !tags.is_empty() {
        format!(
            "{} — {} ({})",
            stem,
            categories.join(", "),
            tags.iter().take(3).cloned().collect::<Vec<_>>().join(", ")
        )
    } else if !categories.is_empty() {
        format!("{} — {}", stem, categories.join(", "))
    } else if !tags.is_empty() {
        format!(
            "{} [{}]",
            stem,
            tags.iter().take(3).cloned().collect::<Vec<_>>().join(", ")
        )
    } else {
        stem
    }
}

