/// Enriched metadata produced by the [`MetadataEnricher`].
#[derive(Debug, Clone, Default)]
pub struct EnrichedMetadata {
    /// Human-readable tags (keywords) discovered for the asset.
    pub tags: Vec<String>,
    /// Confidence scores aligned 1-to-1 with `tags`.
    pub confidence: Vec<f32>,
    /// Optional short auto-generated caption.
    pub auto_caption: Option<String>,
    /// Broad category labels (e.g., "sports", "nature", "documentary").
    pub categories: Vec<String>,
    /// Arbitrary key/value metadata pairs generated by the enrichment rules.
    pub extra: HashMap<String, String>,
}

impl EnrichedMetadata {
    /// Filter `tags` and `confidence` by the given minimum confidence value.
    pub fn filter_by_confidence(&mut self, min: f32) {
        let pairs: Vec<(String, f32)> = self
            .tags
            .drain(..)
            .zip(self.confidence.drain(..))
            .filter(|(_, c)| *c >= min)
            .collect();
        self.tags = pairs.iter().map(|(t, _)| t.clone()).collect();
        self.confidence = pairs.iter().map(|(_, c)| *c).collect();
    }

    /// Merge another `EnrichedMetadata` into this one, deduplicating tags.
    pub fn merge(&mut self, other: EnrichedMetadata) {
        // Merge tags + confidence (avoid duplicates).
        for (tag, conf) in other.tags.into_iter().zip(other.confidence.into_iter()) {
            if !self.tags.contains(&tag) {
                self.tags.push(tag);
                self.confidence.push(conf);
            }
        }
        // Merge categories.
        for cat in other.categories {
            if !self.categories.contains(&cat) {
                self.categories.push(cat);
            }
        }
        // Caption wins if we don't have one.
        if self.auto_caption.is_none() {
            self.auto_caption = other.auto_caption;
        }
        // Merge extra, existing keys win.
        for (k, v) in other.extra {
            self.extra.entry(k).or_insert(v);
        }
    }
}

