/// Orchestrates rule-based and neural enrichment for a media asset.
///
/// # Usage
/// ```rust
/// use oximedia_mam::ai_enrichment::{AiEnrichmentConfig, MetadataEnricher};
///
/// let enricher = MetadataEnricher::new(AiEnrichmentConfig::default());
/// let meta = enricher.enrich("interview_2024.mp4", b"");
/// assert!(meta.categories.contains(&"interview".to_string())
///      || meta.categories.contains(&"video".to_string()));
/// ```
#[derive(Debug, Clone)]
pub struct MetadataEnricher {
    config: AiEnrichmentConfig,
    rule_engine: RuleEngine,
    neural_backend: NeuralBackend,
}

impl MetadataEnricher {
    /// Create a new enricher with the given config and the built-in rule set.
    pub fn new(config: AiEnrichmentConfig) -> Self {
        Self {
            rule_engine: default_rule_engine(),
            neural_backend: NeuralBackend::new(),
            config,
        }
    }

    /// Create an enricher with a custom rule set.
    pub fn with_rules(config: AiEnrichmentConfig, rules: Vec<EnrichmentRule>) -> Self {
        let mut engine = RuleEngine::new();
        for r in rules {
            engine.add_rule(r);
        }
        Self {
            rule_engine: engine,
            neural_backend: NeuralBackend::new(),
            config,
        }
    }

    /// Enrich a media asset identified by `filename` with optional raw bytes
    /// for magic-number detection.
    ///
    /// Steps:
    /// 1. Rule-based enrichment from filename patterns.
    /// 2. Neural classification from raw bytes (if `auto_classify` is set).
    /// 3. Caption generation (if `auto_caption` is set).
    /// 4. Filtering by confidence threshold.
    /// 5. Tag count limiting.
    pub fn enrich(&self, filename: &str, data: &[u8]) -> EnrichedMetadata {
        let mut result = EnrichedMetadata::default();

        // 1. Rule-based tags + categories.
        if self.config.auto_tag || self.config.auto_classify {
            let rule_result = self.rule_engine.apply(filename);
            result.merge(rule_result);
        }

        // 2. Neural classification.
        if self.config.auto_classify {
            let neural = self.neural_backend.classify_bytes(filename, data);
            for (label, score) in neural.labels.into_iter().zip(neural.scores.into_iter()) {
                if !result.tags.contains(&label) {
                    result.tags.push(label.clone());
                    result.confidence.push(score);
                }
                if !result.categories.contains(&label) {
                    result.categories.push(label);
                }
            }
        }

        // 3. Caption.
        if self.config.auto_caption && result.auto_caption.is_none() {
            result.auto_caption =
                Some(generate_caption(filename, &result.tags, &result.categories));
        }

        // 4. Confidence filtering.
        result.filter_by_confidence(self.config.confidence_threshold);

        // 5. Tag limit.
        if result.tags.len() > self.config.max_tags {
            result.tags.truncate(self.config.max_tags);
            result.confidence.truncate(self.config.max_tags);
        }

        result
    }

    /// Enrich with explicit extra metadata key/value pairs (e.g., from sidecar files).
    pub fn enrich_with_extra(
        &self,
        filename: &str,
        data: &[u8],
        extra: HashMap<String, String>,
    ) -> EnrichedMetadata {
        let mut result = self.enrich(filename, data);
        for (k, v) in extra {
            result.extra.insert(k, v);
        }
        result
    }
}

