/// Configuration for the AI enrichment pipeline.
#[derive(Debug, Clone)]
pub struct AiEnrichmentConfig {
    /// Enable automatic tag generation.
    pub auto_tag: bool,
    /// Enable automatic short caption generation.
    pub auto_caption: bool,
    /// Enable automatic high-level category classification.
    pub auto_classify: bool,
    /// Minimum confidence threshold [0.0, 1.0] for a tag/category to be kept.
    pub confidence_threshold: f32,
    /// Maximum number of tags to retain.
    pub max_tags: usize,
}

impl Default for AiEnrichmentConfig {
    fn default() -> Self {
        Self {
            auto_tag: true,
            auto_caption: true,
            auto_classify: true,
            confidence_threshold: 0.3,
            max_tags: 20,
        }
    }
}

impl AiEnrichmentConfig {
    /// Create a fully enabled config with default thresholds.
    pub fn new() -> Self {
        Self::default()
    }

    /// Create a config with all AI features disabled (pass-through mode).
    pub fn disabled() -> Self {
        Self {
            auto_tag: false,
            auto_caption: false,
            auto_classify: false,
            confidence_threshold: 1.0,
            max_tags: 0,
        }
    }
}

