    /// Verify that a second `check_rights` call for the same key does not
    /// create a second database row lookup — if it did, the count in
    /// `rights_grants` would still be 1, meaning the cache served the result.
    ///
    /// We verify cache behaviour indirectly: add a grant, check rights (true,
    /// stored in cache), then revoke the grant and immediately check again —
    /// because `revoke_license` invalidates the cache entry, the second check
    /// must hit the DB and return false.
    #[tokio::test]
    async fn test_rights_cache_hit_avoids_db_query() {
        let (mgr, _dir) = make_manager().await;
        insert_asset(&mgr, "cache-asset-1").await;
        insert_owner(&mgr, "cache-owner-1").await;

        mgr.add_license(
            "cache-lic-1",
            "cache-asset-1",
            "cache-owner-1",
            "royalty-free",
            "2020-01-01T00:00:00Z",
            None,
            None,
        )
        .await
        .expect("add_license");

        // First call: DB miss → stored in cache as `true`.
        let r1 = mgr
            .check_rights("cache-asset-1", None, "2025-01-01T00:00:00Z")
            .await
            .expect("check_rights 1");
        assert!(r1, "expected granted on first call");

        // Second call with same key: should be served from cache.
        let r2 = mgr
            .check_rights("cache-asset-1", None, "2025-01-01T00:00:00Z")
            .await
            .expect("check_rights 2");
        assert!(r2, "expected granted on second (cached) call");

        // Revoke invalidates the cache entry.
        mgr.revoke_license("cache-lic-1")
            .await
            .expect("revoke_license");

        // Third call: cache was invalidated, DB queried, returns false.
        let r3 = mgr
            .check_rights("cache-asset-1", None, "2025-01-01T00:00:00Z")
            .await
            .expect("check_rights after revoke");
        assert!(!r3, "expected denied after revocation (cache invalidated)");
    }

    /// Verify that after `cache_ttl_secs` elapses, `check_rights` re-queries
    /// the database.  We use TTL = 0 to force every call to bypass the cache,
    /// then revoke the license and confirm the next call returns `false`.
    #[tokio::test]
    async fn test_rights_cache_ttl_expiry() {
        let temp_dir = tempfile::tempdir().expect("temp dir should succeed");
        let db_path = format!("sqlite://{}/ttl_test.db", temp_dir.path().display());

        // TTL = 0 means caching is effectively disabled (every call is a miss).
        let config = RightsManagerConfig {
            pool_size: 2,
            connect_timeout_secs: 5,
            cache_ttl_secs: 0,
        };
        let mgr = RightsManager::new_with_config(&db_path, config)
            .await
            .expect("new_with_config");

        insert_asset(&mgr, "ttl-asset").await;
        insert_owner(&mgr, "ttl-owner").await;
        mgr.add_license(
            "ttl-lic",
            "ttl-asset",
            "ttl-owner",
            "royalty-free",
            "2020-01-01T00:00:00Z",
            None,
            None,
        )
        .await
        .expect("add_license");

        // First call: DB queried, returns true.
        let r1 = mgr
            .check_rights("ttl-asset", None, "2025-01-01T00:00:00Z")
            .await
            .expect("check_rights 1");
        assert!(r1, "expected granted");

        // Revoke license directly via pool (bypasses cache invalidation) —
        // with TTL=0 the next check_rights will always re-query the DB anyway.
        sqlx::query("DELETE FROM rights_grants WHERE id = ?")
            .bind("ttl-lic")
            .execute(mgr.database().pool())
            .await
            .expect("direct delete");

        // Second call: with TTL=0 there is no caching, so DB is queried → false.
        let r2 = mgr
            .check_rights("ttl-asset", None, "2025-01-01T00:00:00Z")
            .await
            .expect("check_rights 2");
        assert!(!r2, "expected denied after TTL=0 re-query");
    }
