/// Result type for rights management operations
pub type Result<T> = std::result::Result<T, RightsError>;

/// Errors that can occur in rights management operations
#[derive(Error, Debug)]
pub enum RightsError {
    /// Database error
    #[cfg(not(target_arch = "wasm32"))]
    #[error("Database error: {0}")]
    Database(#[from] sqlx::Error),

    /// Rights not found
    #[error("Rights not found: {0}")]
    NotFound(String),

    /// Rights expired
    #[error("Rights expired: {0}")]
    Expired(String),

    /// Territory restriction violation
    #[error("Territory restriction violation: {0}")]
    TerritoryViolation(String),

    /// Usage restriction violation
    #[error("Usage restriction violation: {0}")]
    UsageViolation(String),

    /// License not valid
    #[error("License not valid: {0}")]
    InvalidLicense(String),

    /// Clearance not obtained
    #[error("Clearance not obtained: {0}")]
    ClearanceRequired(String),

    /// Royalty calculation error
    #[error("Royalty calculation error: {0}")]
    RoyaltyError(String),

    /// Watermark error
    #[error("Watermark error: {0}")]
    WatermarkError(String),

    /// DRM error
    #[error("DRM error: {0}")]
    DrmError(String),

    /// Compliance violation
    #[error("Compliance violation: {0}")]
    ComplianceViolation(String),

    /// Invalid operation
    #[error("Invalid operation: {0}")]
    InvalidOperation(String),

    /// Serialization error
    #[error("Serialization error: {0}")]
    Serialization(String),

    /// IO error
    #[error("IO error: {0}")]
    Io(#[from] std::io::Error),
}

// ── RightsManagerConfig ───────────────────────────────────────────────────────

/// Configuration for [`RightsManager`].
///
/// Controls the SQLite connection pool and the rights-check result cache.
#[cfg(not(target_arch = "wasm32"))]
#[derive(Debug, Clone)]
pub struct RightsManagerConfig {
    /// Maximum number of SQLite pool connections.  Defaults to `5`.
    pub pool_size: u32,
    /// Timeout in seconds when acquiring a pool connection.  Defaults to `5`.
    pub connect_timeout_secs: u64,
    /// Time-to-live in seconds for cached rights-check results.  Defaults to `60`.
    pub cache_ttl_secs: u64,
}

#[cfg(not(target_arch = "wasm32"))]
impl Default for RightsManagerConfig {
    fn default() -> Self {
        Self {
            pool_size: 5,
            connect_timeout_secs: 5,
            cache_ttl_secs: 60,
        }
    }
}

// ── RightsCheckCache ──────────────────────────────────────────────────────────

/// A lightweight in-process cache for `check_rights` results.
///
/// Cache key: `(asset_id, territory_or_none, at_date)`.
/// Each entry expires after `ttl_secs` seconds of wall-clock time.
#[cfg(not(target_arch = "wasm32"))]
struct RightsCheckCache {
    store: HashMap<(String, String, String), (bool, Instant)>,
    ttl_secs: u64,
}

#[cfg(not(target_arch = "wasm32"))]
impl RightsCheckCache {
    fn new(ttl_secs: u64) -> Self {
        Self {
            store: HashMap::new(),
            ttl_secs,
        }
    }

    /// Look up a cached result.  Returns `None` when no entry exists or the
    /// entry has expired (stale entries are removed on access).
    fn get(&mut self, asset_id: &str, territory: Option<&str>, at_date: &str) -> Option<bool> {
        let key = (
            asset_id.to_string(),
            territory.unwrap_or("*").to_string(),
            at_date.to_string(),
        );
        match self.store.get(&key) {
            Some((value, inserted_at)) if inserted_at.elapsed().as_secs() < self.ttl_secs => {
                Some(*value)
            }
            Some(_) => {
                // Stale — evict eagerly.
                self.store.remove(&key);
                None
            }
            None => None,
        }
    }

    /// Store a result.
    fn set(&mut self, asset_id: &str, territory: Option<&str>, at_date: &str, value: bool) {
        let key = (
            asset_id.to_string(),
            territory.unwrap_or("*").to_string(),
            at_date.to_string(),
        );
        self.store.insert(key, (value, Instant::now()));
    }

    /// Remove all entries for `asset_id` so that a revocation takes effect
    /// immediately without waiting for TTL expiry.
    fn invalidate(&mut self, asset_id: &str) {
        self.store.retain(|(a, _, _), _| a != asset_id);
    }
}

// ── RightsManager ─────────────────────────────────────────────────────────────

/// Main rights management system
#[cfg(not(target_arch = "wasm32"))]
pub struct RightsManager {
    db: database::RightsDatabase,
    config: RightsManagerConfig,
    cache: Arc<Mutex<RightsCheckCache>>,
}

#[cfg(not(target_arch = "wasm32"))]
impl RightsManager {
    /// Create a new rights manager with the specified database path.
    ///
    /// Uses [`RightsManagerConfig::default`] for pool and cache settings.
    pub async fn new(database_path: &str) -> Result<Self> {
        Self::new_with_config(database_path, RightsManagerConfig::default()).await
    }

    /// Create a new rights manager with explicit configuration.
    pub async fn new_with_config(database_path: &str, config: RightsManagerConfig) -> Result<Self> {
        let db = database::RightsDatabase::new_with_pool(
            database_path,
            config.pool_size,
            config.connect_timeout_secs,
        )
        .await?;
        let cache = Arc::new(Mutex::new(RightsCheckCache::new(config.cache_ttl_secs)));
        Ok(Self { db, config, cache })
    }

    /// Get reference to the database
    pub fn database(&self) -> &database::RightsDatabase {
        &self.db
    }

    /// Get mutable reference to the database
    pub fn database_mut(&mut self) -> &mut database::RightsDatabase {
        &mut self.db
    }

    /// Get the current configuration.
    pub fn config(&self) -> &RightsManagerConfig {
        &self.config
    }

    // ── High-level license API ────────────────────────────────────────────────

    /// Add a license grant record to the database.
    ///
    /// Inserts a row into `rights_grants`.  The `asset_id` must already exist
    /// in the `assets` table.
    ///
    /// # Parameters
    /// - `license_id` — globally unique identifier for this license.
    /// - `asset_id` — the asset being licensed.
    /// - `owner_id` — the rights owner granting the license.
    /// - `license_type` — e.g. `"royalty-free"`, `"rights-managed"`.
    /// - `start_date` — ISO 8601 / RFC 3339 start date string.
    /// - `end_date` — optional end date; `None` means perpetual.
    /// - `territory_json` — optional JSON array of territory codes.
    #[allow(clippy::too_many_arguments)]
    pub async fn add_license(
        &self,
        license_id: &str,
        asset_id: &str,
        owner_id: &str,
        license_type: &str,
        start_date: &str,
        end_date: Option<&str>,
        territory_json: Option<&str>,
    ) -> Result<()> {
        let now = Utc::now().to_rfc3339();
        sqlx::query(
            r"INSERT INTO rights_grants
              (id, asset_id, owner_id, license_type, start_date, end_date,
               is_exclusive, territory_json, created_at, updated_at)
              VALUES (?, ?, ?, ?, ?, ?, 0, ?, ?, ?)",
        )
        .bind(license_id)
        .bind(asset_id)
        .bind(owner_id)
        .bind(license_type)
        .bind(start_date)
        .bind(end_date)
        .bind(territory_json)
        .bind(&now)
        .bind(&now)
        .execute(self.db.pool())
        .await?;
        Ok(())
    }

    /// Revoke (delete) a license by its ID.
    ///
    /// Returns `Err(RightsError::NotFound)` if no row with that ID exists.
    ///
    /// Also invalidates any cache entries for the affected asset so that
    /// subsequent [`check_rights`](Self::check_rights) calls reflect the
    /// revocation immediately.
    pub async fn revoke_license(&self, license_id: &str) -> Result<()> {
        // Fetch the asset_id first so we can invalidate the cache after deletion.
        let asset_id_row: Option<(String,)> =
            sqlx::query_as("SELECT asset_id FROM rights_grants WHERE id = ?")
                .bind(license_id)
                .fetch_optional(self.db.pool())
                .await?;

        let rows = sqlx::query("DELETE FROM rights_grants WHERE id = ?")
            .bind(license_id)
            .execute(self.db.pool())
            .await?;
        if rows.rows_affected() == 0 {
            return Err(RightsError::NotFound(format!(
                "License not found: {license_id}"
            )));
        }

        // Invalidate cached results for this asset.
        if let Some((asset_id,)) = asset_id_row {
            let mut guard = self.cache.lock().await;
            guard.invalidate(&asset_id);
        }
        Ok(())
    }

    /// Check whether an active, unexpired license exists for `asset_id` in
    /// `territory` at `at_date` (ISO 8601 string).
    ///
    /// Results are cached for [`RightsManagerConfig::cache_ttl_secs`] seconds
    /// to reduce repeated database round-trips for hot assets.  The cache is
    /// invalidated automatically when `revoke_license` is called.
    ///
    /// Returns `true` if at least one matching license exists.
    pub async fn check_rights(
        &self,
        asset_id: &str,
        territory: Option<&str>,
        at_date: &str,
    ) -> Result<bool> {
        // Check cache first.
        {
            let mut guard = self.cache.lock().await;
            if let Some(cached) = guard.get(asset_id, territory, at_date) {
                return Ok(cached);
            }
        }

        // Cache miss — query the database.
        let row: (i64,) = sqlx::query_as(
            r"SELECT COUNT(*) FROM rights_grants
              WHERE asset_id = ?
                AND start_date <= ?
                AND (end_date IS NULL OR end_date >= ?)
                AND (territory_json IS NULL OR territory_json LIKE ?)",
        )
        .bind(asset_id)
        .bind(at_date)
        .bind(at_date)
        .bind(
            territory
                .map(|t| format!("%{t}%"))
                .unwrap_or_else(|| "%".to_string()),
        )
        .fetch_one(self.db.pool())
        .await?;
        let result = row.0 > 0;

        // Store in cache.
        {
            let mut guard = self.cache.lock().await;
            guard.set(asset_id, territory, at_date, result);
        }
        Ok(result)
    }

    /// List all active licenses for `asset_id` valid at `at_date`.
    ///
    /// Returns a `Vec` of `(id, license_type, start_date, end_date)` tuples.
    pub async fn list_active_licenses(
        &self,
        asset_id: &str,
        at_date: &str,
    ) -> Result<Vec<(String, String, String, Option<String>)>> {
        let rows: Vec<(String, String, String, Option<String>)> = sqlx::query_as(
            r"SELECT id, license_type, start_date, end_date
              FROM rights_grants
              WHERE asset_id = ?
                AND start_date <= ?
                AND (end_date IS NULL OR end_date >= ?)",
        )
        .bind(asset_id)
        .bind(at_date)
        .bind(at_date)
        .fetch_all(self.db.pool())
        .await?;
        Ok(rows)
    }

    /// Batch rights check: for each `asset_id` in the slice, check whether
    /// a valid license exists using a single `WHERE asset_id IN (…)` query.
    ///
    /// Returns a `HashMap<asset_id, bool>` (true = at least one valid license).
    pub async fn check_rights_batch(
        &self,
        asset_ids: &[&str],
        territory: Option<&str>,
        at_date: &str,
    ) -> Result<std::collections::HashMap<String, bool>> {
        if asset_ids.is_empty() {
            return Ok(std::collections::HashMap::new());
        }

        // Build the IN clause placeholders.
        let placeholders: String = asset_ids.iter().map(|_| "?").collect::<Vec<_>>().join(", ");
        let sql = format!(
            r"SELECT asset_id, COUNT(*) as cnt
              FROM rights_grants
              WHERE asset_id IN ({placeholders})
                AND start_date <= ?
                AND (end_date IS NULL OR end_date >= ?)
                AND (territory_json IS NULL OR territory_json LIKE ?)
              GROUP BY asset_id"
        );

        let territory_pattern = territory
            .map(|t| format!("%{t}%"))
            .unwrap_or_else(|| "%".to_string());

        // Build query with bound parameters.
        let mut query = sqlx::query_as::<_, (String, i64)>(&sql);
        for id in asset_ids {
            query = query.bind(*id);
        }
        query = query.bind(at_date).bind(at_date).bind(territory_pattern);

        let rows: Vec<(String, i64)> = query.fetch_all(self.db.pool()).await?;

        // Seed with false for all requested IDs, then flip to true if found.
        let mut result: std::collections::HashMap<String, bool> = asset_ids
            .iter()
            .map(|&id| (id.to_string(), false))
            .collect();
        for (asset_id, count) in rows {
            if count > 0 {
                result.insert(asset_id, true);
            }
        }
        Ok(result)
    }
}

