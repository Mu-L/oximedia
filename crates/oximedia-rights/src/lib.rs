//! Content Rights and Licensing Management for OxiMedia
//!
//! This crate provides comprehensive rights management capabilities including:
//! - Rights tracking and ownership management
//! - License management with various types (royalty-free, rights-managed, etc.)
//! - Expiration tracking and renewal management
//! - Geographic territory restrictions
//! - Usage tracking and reporting
//! - Clearance tracking (music, footage, talent)
//! - Royalty calculation and payment tracking
//! - Watermarking integration
//! - DRM metadata management
//! - Audit trail and compliance reporting
//!
//! ## Data Model
//!
//! ```text
//! RightsHolder
//!   └─ License (rights grant period + terms)
//!       ├─ Territory (list of allowed regions)
//!       └─ Usage (stream/download/broadcast limits)
//! ```
//!
//! A `RightsHolder` represents any entity (individual, label, studio, or PRO)
//! that owns or controls rights to a media asset.  Each `RightsHolder` may hold
//! one or more `License` records that specify the legal grant: the time window
//! during which the rights are valid, the agreed terms (royalty rates, exclusivity,
//! etc.), and the permitted territories and usage types.  `Territory` constrains
//! distribution to specific geographic regions (ISO 3166-1 alpha-2 codes or
//! aggregate zones).  `Usage` defines what the licensee may do — stream, download,
//! broadcast, or combinations thereof — and any volumetric limits.
//!
//! ## Rights Check Flow
//!
//! ```text
//! Asset request
//!      │
//!      ▼
//! Territory check ── DENIED ──►  403 Territory
//!      │ OK
//!      ▼
//! Expiry check ─────DENIED ──►  403 Expired
//!      │ OK
//!      ▼
//! Usage type check ─DENIED ──►  403 Usage
//!      │ OK
//!      ▼
//! Clearance check ──DENIED ──►  403 Clearance
//!      │ OK
//!      ▼
//!     200 OK
//! ```
//!
//! ## DRM Integration
//!
//! To link Widevine, PlayReady, or FairPlay protection to a rights record, store
//! the DRM content key identifier in the license record:
//!
//! ```text
//! License {
//!     content_key_id: Uuid,   // PSSH/KID from the DRM system
//!     ...
//! }
//! ```
//!
//! The `content_key_id` field holds the UUID that corresponds to the PSSH
//! (Protection System Specific Header) `KID` (Key ID) value generated by your
//! DRM server.  Cross-reference `oximedia-drm` for key lifecycle management:
//! key generation, rotation, and license server configuration for each DRM
//! scheme (Widevine CENC, PlayReady PIFF, FairPlay HLS).  When a rights record
//! is revoked or expired the associated `content_key_id` should be invalidated
//! in the DRM key management system to prevent further playback.

