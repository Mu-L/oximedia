/// Motion vector reference frame modes.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RefFrame {
    /// Previous frame.
    Last,
    /// Golden reference frame.
    Golden,
}

impl RefFrame {
    /// Get reference frame index.
    #[must_use]
    pub const fn index(self) -> usize {
        match self {
            Self::Last => 0,
            Self::Golden => 1,
        }
    }
}

/// Coding mode for a block.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum CodingMode {
    /// Intra-coded (keyframe block).
    Intra,
    /// Inter-coded with motion vector from last frame.
    InterMv,
    /// Inter-coded with no motion vector (copy from last frame).
    InterNoMv,
    /// Inter-coded with motion vector from golden frame.
    InterGoldenMv,
    /// Inter-coded with no motion vector from golden frame.
    InterGoldenNoMv,
    /// Inter-coded with last motion vector.
    InterLastMv,
    /// Inter-coded with prior motion vector.
    InterPrior,
    /// Not coded (copy from previous frame).
    NotCoded,
}

impl CodingMode {
    /// Check if this is an inter mode.
    #[must_use]
    pub const fn is_inter(self) -> bool {
        !matches!(self, Self::Intra | Self::NotCoded)
    }

    /// Check if this mode uses a motion vector.
    #[must_use]
    pub const fn has_motion_vector(self) -> bool {
        matches!(
            self,
            Self::InterMv | Self::InterGoldenMv | Self::InterLastMv | Self::InterPrior
        )
    }

    /// Get the reference frame for this mode.
    #[must_use]
    pub const fn ref_frame(self) -> Option<RefFrame> {
        match self {
            Self::InterMv
            | Self::InterNoMv
            | Self::InterLastMv
            | Self::InterPrior
            | Self::NotCoded => Some(RefFrame::Last),
            Self::InterGoldenMv | Self::InterGoldenNoMv => Some(RefFrame::Golden),
            Self::Intra => None,
        }
    }
}

/// Theora pixel format.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum TheoraPixelFormat {
    /// 4:2:0 subsampling.
    Yuv420,
    /// 4:2:2 subsampling.
    Yuv422,
    /// 4:4:4 (no subsampling).
    Yuv444,
}

impl TheoraPixelFormat {
    /// Get horizontal chroma subsampling factor.
    #[must_use]
    pub const fn h_shift(self) -> u32 {
        match self {
            Self::Yuv420 | Self::Yuv422 => 1,
            Self::Yuv444 => 0,
        }
    }

    /// Get vertical chroma subsampling factor.
    #[must_use]
    pub const fn v_shift(self) -> u32 {
        match self {
            Self::Yuv420 => 1,
            Self::Yuv422 | Self::Yuv444 => 0,
        }
    }

    /// Get number of chroma blocks per macroblock.
    #[must_use]
    pub const fn chroma_blocks(self) -> usize {
        match self {
            Self::Yuv420 => 2, // 1 Cb + 1 Cr
            Self::Yuv422 => 4, // 2 Cb + 2 Cr
            Self::Yuv444 => 8, // 4 Cb + 4 Cr
        }
    }
}

